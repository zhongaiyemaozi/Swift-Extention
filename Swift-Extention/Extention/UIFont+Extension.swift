import UIKit

extension UIFont {
    
    public static func cl_PingFangSCRegular(_ fontSize:CGFloat) -> UIFont {
        return UIFont.init(name: "PingFangSC-Regular", size: fontSize)!
    }
    
    public static  func cl_PingFangSCLight(_ fontSize:CGFloat) -> UIFont{
        return UIFont.init(name: "PingFangSC-Light", size: fontSize)!
    }
    public static  func cl_PingFangSCThin(_ fontSize:CGFloat) -> UIFont{
        return UIFont.init(name: "PingFangSC-Thin", size: fontSize)!
    }
    public static  func cl_PingFangSCMeaium(_ fontSize:CGFloat) -> UIFont{
        return UIFont.init(name: "PingFangSC-Medium", size: fontSize)!
    }
    public static  func cl_SFNSDisplay(_ fontSize:CGFloat) -> UIFont{
        return UIFont.init(name: "SFNSDisplay", size: fontSize)!
    }
    public static  func cl_DINProMedium(_ fontSize:CGFloat) -> UIFont{
        return UIFont.init(name: "DINPro-Medium", size: fontSize)!
    }
    
    
    
    
}
